package edu.crypto2.components;

public class LinesOut {
	 
	private String line0_before;
	private String line1_before;
	private String line2_before;
	private String line3_before;
	
	private String line0;
	private String line1;
	private String line2;
	private String line3;
	
	public LinesOut(String line0_before, String line1_before, String line2_before, String line3_before,
					String line0, String line1, String line2, String line3){
		this.line0_before = line0_before;
		this.line1_before = line1_before;
		this.line2_before = line2_before;
		this.line3_before = line3_before;

		this.line0 = line0;
		this.line1 = line1;
		this.line2 = line2;
		this.line3 = line3;
	}

	/**
	 * @return the line0_before
	 */
	public String getLine0_before() {
		return line0_before;
	}
	/**
	 * @param line0Before the line0_before to set
	 */
	public void setLine0_before(String line0Before) {
		this.line0_before = line0Before;
	}
	/**
	 * @return the line1_before
	 */
	public String getLine1_before() {
		return line1_before;
	}
	/**
	 * @param line1Before the line1_before to set
	 */
	public void setLine1_before(String line1Before) {
		this.line1_before = line1Before;
	}
	/**
	 * @return the line2_before
	 */
	public String getLine2_before() {
		return line2_before;
	}
	/**
	 * @param line2Before the line2_before to set
	 */
	public void setLine2_before(String line2Before) {
		this.line2_before = line2Before;
	}
	/**
	 * @return the line3_before
	 */
	public String getLine3_before() {
		return line3_before;
	}
	/**
	 * @param line3Before the line3_before to set
	 */
	public void setLine3_before(String line3Before) {
		this.line3_before = line3Before;
	}
	/**
	 * @return the line0
	 */
	public String getLine0() {
		return line0;
	}
	/**
	 * @param line0 the line0 to set
	 */
	public void setLine0(String line0) {
		this.line0 = line0;
	}
	/**
	 * @return the line1
	 */
	public String getLine1() {
		return line1;
	}
	/**
	 * @param line1 the line1 to set
	 */
	public void setLine1(String line1) {
		this.line1 = line1;
	}
	/**
	 * @return the line2
	 */
	public String getLine2() {
		return line2;
	}
	/**
	 * @param line2 the line2 to set
	 */
	public void setLine2(String line2) {
		this.line2 = line2;
	}
	/**
	 * @return the line3
	 */
	public String getLine3() {
		return line3;
	}
	/**
	 * @param line3 the line3 to set
	 */
	public void setLine3(String line3) {
		this.line3 = line3;
	}


}
